package casting;

public class Ex02 {

	public static void main(String[] args) {
		System.out.println(10 / 3);
		System.out.println(10 / 3.0);
		// 형변환 발생 : 부동소수점 생기긴 함 : 단순오류
		
		
		int n1 = 10, n2 = 3;
		
		System.out.println(n1 / n2);
		
		System.out.println(n1 / (double)n2);
		// 바꾸고자하는 변수 앞에 괄호치고 자료형 선언
		
		
		
		
				
		
	}

}
