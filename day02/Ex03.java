package day02;

public class Ex03 {

	public static void main(String[] args) {
		// 자료형 : 데이터 타입
		// 1. 문자열 : " "큰 따옴표
		// 2. 문자 : ' ' 홋 따옴표, 글자 하나만 표현함
		// 3. 정수 : 따옴표가 없는 숫자 데이터, 연산
		// 4. 실수 : 소수점 있는 데이터
		
		System.out.println("10" + "20");
		System.out.println("Apple" + "Banana");   //문자열사이에 + 적으면 문자열 2개 이어 붙이기
		
		//정수와 실수는 연산을 위한 타입들
		System.out.println(10 + 20);
		System.out.println(10.5 + 3.141592);
		
	}

}
