package loop;

public class Ex01 {

	public static void main(String[] args) {
		// 반복문 while
		
		int i = 1;
		
		while (i <= 5) {
			System.out.println(i + "Hello World");
			i++;
		}
		System.out.println("반복 끝");
		
		int n = 5;
		
		while (n >= 1) {
			System.out.println(i + "Hello World");
			n--;
		}
		
	}

}
